    print(f"  Platforms: {platforms_str}")
    print(f"  Functions: {len(configs)} ({len(aws_funcs)} AWS, {len(gcp_funcs)} GCP)")
    print(f"  Datastore: {ds_display}")
    if multi_cloud:
        print(f"  Mode: \033[33mmulti-cloud\033[0m (cross-platform HTTP invocation)")
    print()
    
    # ── Phase 1: Resolve functions to identifiers (ARNs, URLs) ──────────────
    
    # enriched_map: {func_name: {"platform": ..., "arn": ..., "url": ...}}
    enriched_map = {}
    
    # Resolve AWS functions
    for func_name in aws_funcs:
        func_ref = functions_map[func_name]
        entry = functions_full[func_name]
        
