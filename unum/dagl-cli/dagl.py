    configs = compiled["configs"]
    template = compiled["template"]
    functions_map = compiled["functions_map"]
    
    dagl_config = load_dagl_config(args.config or "dagl.yaml")
    _, functions_full = get_functions_map(dagl_config)
    
    # If compiled didn't have functions_full, rebuild from dagl.yaml
    if "functions_full" in compiled:
        functions_full = compiled["functions_full"]
    
    region = dagl_config.get("region", "eu-central-1")
    explicit_layer_arn = args.layer_arn or dagl_config.get("layer_arn")
    checkpoint = dagl_config.get("checkpoint", True)
    workflow_name = template.get("Globals", {}).get("ApplicationName", "dagl-workflow")
    
    # Determine datastore config
    multi_cloud = is_multi_cloud_workflow(functions_full)
    datastore_cfg = dagl_config.get("datastore", {})
    
    if multi_cloud:
        # Multi-cloud workflows MUST use a shared datastore (Redis)
        ds_type = datastore_cfg.get("type", "redis")
        ds_name = datastore_cfg.get("url", dagl_config.get("datastore_name", DEFAULT_DS_NAME))
