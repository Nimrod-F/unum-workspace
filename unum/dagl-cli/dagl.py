def _get_registry(dagl_config):
    """Create a Registry from dagl.yaml 'registry' config. Returns None if not configured."""
    reg_cfg = dagl_config.get("registry")
    if not reg_cfg:
        return None
    try:
        from registry import Registry
        if "url" in reg_cfg:
            return Registry(url=reg_cfg["url"])
        return Registry(
            host=reg_cfg.get("host", "localhost"),
            port=reg_cfg.get("port", 6379),
            db=reg_cfg.get("db", 0),
            password=reg_cfg.get("password"),
        )
    except ImportError:
        print("\033[33mWarning:\033[0m redis package not installed. Run: pip install redis")
        return None
    except Exception as e:
        print(f"\033[33mWarning:\033[0m Cannot connect to registry: {e}")
        return None


