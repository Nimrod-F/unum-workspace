    # For single-cloud AWS workflows, also build legacy ARN map for backwards compat
    if not multi_cloud and not gcp_funcs:
        arn_map = {k: v["arn"] for k, v in enriched_map.items()}
        function_map_json = json.dumps(arn_map)
    
    # Determine FAAS_PLATFORM for env var
    faas_platform = "multi" if multi_cloud else "aws"
    
    # Initialize registry if configured
    registry = _get_registry(dagl_config)
