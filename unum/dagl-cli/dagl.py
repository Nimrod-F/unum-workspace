def _deploy_eventbridge(lambda_client, region, event_config, configs, arn_map, workflow_name):
    """Create EventBridge rule targeting the start function."""
    import boto3
    
    events_client = boto3.client("events", region_name=region)
    
    # Find start function
    start_func = None
    for name, cfg in configs.items():
        if cfg.get("Start", False):
            start_func = name
            break
    
    if not start_func:
        return
    
    start_arn = arn_map[start_func]
    source = event_config.get("source", "custom")
    detail_type = event_config.get("detailType")
    rule_name = f"dagl-{workflow_name}-trigger"
    
    # Build event pattern
    pattern = {"source": [source]}
    if detail_type:
        pattern["detail-type"] = [detail_type]
    
    # Create/update EventBridge rule
    events_client.put_rule(
        Name=rule_name,
        EventPattern=json.dumps(pattern),
        State="ENABLED",
        Description=f"DAGL workflow trigger for {workflow_name}",
    )
    
    # Add target
    events_client.put_targets(
        Rule=rule_name,
        Targets=[{"Id": f"dagl-{start_func}", "Arn": start_arn}]
    )
    
    # Add permission for EventBridge to invoke the Lambda
    try:
        lambda_client.add_permission(
            FunctionName=start_arn,
            StatementId=f"dagl-eventbridge-{workflow_name}",
            Action="lambda:InvokeFunction",
            Principal="events.amazonaws.com",
            SourceArn=f"arn:aws:events:{region}:*:rule/{rule_name}",
        )
    except lambda_client.exceptions.ResourceConflictException:
        pass  # Permission already exists
    
    print(f"\n  \033[32m✓\033[0m EventBridge: {source} / {detail_type or '*'} → {start_func}")


