def _undeploy_gcp_function(func_ref, entry):
    """Restore a GCP Cloud Function to its original entry point and env vars."""
    import subprocess
    
    gcloud_cmd = _find_gcloud()
    gcp_project = entry.get("project", "")
    gcp_region = entry.get("region", "europe-west1")
    original_handler = entry.get("handler", "handler")
    # Extract just the function name from module.function format
    original_entry = original_handler.rsplit(".", 1)[-1] if "." in original_handler else original_handler
    
    dagl_vars = ["DAGL_USER_HANDLER", "DAGL_CONFIG", "DAGL_FUNCTION_MAP",
                 "FAAS_PLATFORM", "FAAS_PLATFORM_NATIVE",
                 "UNUM_INTERMEDIARY_DATASTORE_TYPE",
                 "UNUM_INTERMEDIARY_DATASTORE_NAME", "WORKFLOW_NAME",
                 "CHECKPOINT", "GC"]
    
    cmd = [
        gcloud_cmd, "functions", "deploy", func_ref,
        f"--region={gcp_region}",
        f"--entry-point={original_entry}",
        f"--remove-env-vars={','.join(dagl_vars)}",
        "--trigger-http",
        "--allow-unauthenticated",
        "--quiet",
    ]
    
    source = entry.get("source")
    if source:
        cmd.append(f"--source={source}")
    
    if gcp_project:
        cmd.append(f"--project={gcp_project}")
    
    result = subprocess.run(cmd, capture_output=True, text=True, timeout=300)
    
    if result.returncode != 0:
        error_msg = result.stderr.strip() or result.stdout.strip()
        raise RuntimeError(error_msg)


