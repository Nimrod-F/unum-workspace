def cmd_undeploy(args):
    """Remove DAGL orchestration — restore original function configs."""
    import boto3
    
    dagl_config = load_dagl_config(args.config or "dagl.yaml")
    region = dagl_config.get("region", "eu-central-1")
    functions_map, functions_full = get_functions_map(dagl_config)
    workflow_name = dagl_config.get("workflow_name", "dagl-workflow")
    
    lambda_client = boto3.client("lambda", region_name=region)
    
    print(f"\033[36mRemoving DAGL orchestration...\033[0m\n")
    
    for func_name, func_ref in functions_map.items():
        entry = functions_full[func_name]
        platform = entry["platform"]
        
        if platform == "gcp":
            print(f"  Restoring {func_name} ({func_ref}) [GCP]...")
            try:
                _undeploy_gcp_function(func_ref, entry)
                print(f"    \033[32m✓\033[0m GCP function restored")
            except Exception as e:
                print(f"    \033[33m!\033[0m GCP restore skipped: {e}")
            continue
        
        print(f"  Restoring {func_name} ({func_ref})...")
        
        try:
            current = lambda_client.get_function_configuration(FunctionName=func_ref)
        except Exception as e:
            print(f"    \033[31m✗\033[0m Cannot read function: {e}")
            continue
        
        current_env = current.get("Environment", {}).get("Variables", {})
        original_handler = current_env.get("DAGL_USER_HANDLER")
        
        if not original_handler:
            print(f"    Skipped (not managed by DAGL)")
            continue
        
        # Remove DAGL env vars
        dagl_vars = ["DAGL_USER_HANDLER", "DAGL_CONFIG", "DAGL_FUNCTION_MAP",
                     "FAAS_PLATFORM", "FAAS_PLATFORM_NATIVE",
                     "UNUM_INTERMEDIARY_DATASTORE_TYPE",
                     "UNUM_INTERMEDIARY_DATASTORE_NAME", "WORKFLOW_NAME",
                     "CHECKPOINT", "GC"]
        new_env = {k: v for k, v in current_env.items() if k not in dagl_vars}
        
        # Remove DAGL layer (python or nodejs)
        current_layers = [l["Arn"] for l in current.get("Layers", [])]
        new_layers = [l for l in current_layers if "dagl-runtime-python" not in l and "dagl-runtime-nodejs" not in l]
        
        lambda_client.update_function_configuration(
            FunctionName=func_ref,
            Handler=original_handler,
            Layers=new_layers,
            Environment={"Variables": new_env},
        )
        print(f"    \033[32m✓\033[0m Restored handler: {original_handler}")
        
        # Remove Node.js wrapper if present
        runtime = current.get("Runtime", "")
        if runtime.startswith("nodejs"):
            try:
                # Wait for config update to complete before modifying code
                waiter = lambda_client.get_waiter('function_updated_v2')
                waiter.wait(FunctionName=func_ref, WaiterConfig={'Delay': 2, 'MaxAttempts': 30})
                _remove_nodejs_wrapper(lambda_client, func_ref)
                print(f"    \033[32m✓\033[0m Wrapper removed (dagl_wrapper.js)")
            except Exception:
                pass  # Wrapper may not exist
    
    # Remove EventBridge rule
    try:
        events_client = boto3.client("events", region_name=region)
        rule_name = f"dagl-{workflow_name}-trigger"
        
        # Remove targets first
        events_client.remove_targets(Rule=rule_name, Ids=[f"dagl-{func_name}" for func_name in functions_map])
        events_client.delete_rule(Name=rule_name)
        print(f"\n  \033[32m✓\033[0m EventBridge rule removed")
    except Exception:
        pass
    
    print(f"\n\033[32mUndeploy complete.\033[0m Functions restored to original state.")


