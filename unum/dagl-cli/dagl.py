def normalize_function_entry(func_value, default_platform="aws", default_region="eu-central-1"):
    """Normalize a function entry from dagl.yaml into a standard dict format.
    
    Accepts:
      - String: "my-function-name" → {"name": "my-function-name", "platform": "aws", "region": default}
      - Dict: {"name": "...", "platform": "gcp", ...} → passed through with defaults filled
    
    For GCP functions, auto-detects:
      - project: from gcloud config
      - region: defaults to "europe-west1"
      - runtime: inferred from source directory (requirements.txt → python312, package.json → nodejs20)
      - handler: defaults to "main.handler"
      - url: constructed from region + project + function name
    
    Returns dict with keys: name, platform, region, and optionally: project, url, arn, source, etc.
    """
    if isinstance(func_value, str):
        return {
            "name": func_value,
            "platform": default_platform,
            "region": default_region,
        }
    elif isinstance(func_value, dict):
        entry = dict(func_value)
        entry.setdefault("platform", default_platform)
        
        if entry["platform"] == "gcp":
            # GCP defaults
            entry.setdefault("region", "europe-west1")
            entry.setdefault("handler", "main.handler")
            
            # Auto-detect project from gcloud config if not specified
            if "project" not in entry:
                entry["project"] = _detect_gcp_project()
            
            # Auto-detect runtime from source directory
            if "runtime" not in entry and entry.get("source"):
                entry["runtime"] = _detect_gcp_runtime(entry["source"])
            entry.setdefault("runtime", "python312")
            
            # Auto-construct URL if not specified
            if "url" not in entry and entry.get("project"):
                region = entry["region"]
                project = entry["project"]
                name = entry.get("name", "")
                entry["url"] = f"https://{region}-{project}.cloudfunctions.net/{name}"
        else:
            entry.setdefault("region", default_region)
        
        if "name" not in entry:
            raise ValueError(f"Function entry must have a 'name' field: {func_value}")
        return entry
    else:
        raise ValueError(f"Invalid function entry (expected string or dict): {func_value}")


def _detect_gcp_project():
    """Auto-detect GCP project from gcloud config."""
    import subprocess
    try:
        gcloud_cmd = _find_gcloud()
        result = subprocess.run(
            [gcloud_cmd, "config", "get-value", "project"],
            capture_output=True, text=True, timeout=10
        )
        project = result.stdout.strip()
        if project and project != "(unset)":
            return project
    except Exception:
        pass
    return ""


def _detect_gcp_runtime(source_dir):
    """Infer GCP runtime from source directory contents."""
    if not os.path.isdir(source_dir):
        return "python312"
    if os.path.exists(os.path.join(source_dir, "requirements.txt")):
        return "python312"
    if os.path.exists(os.path.join(source_dir, "package.json")):
        return "nodejs20"
    # Check for .py or .js files
    for f in os.listdir(source_dir):
        if f.endswith(".py"):
            return "python312"
        if f.endswith(".js") or f.endswith(".ts"):
            return "nodejs20"
    return "python312"


def get_functions_map(dagl_config):
    """Extract and normalize the functions mapping from dagl config.
    
    Returns:
      functions_map: dict of {workflow_name: function_name_string} (for backwards compat)
      functions_full: dict of {workflow_name: normalized_dict} (full platform info)
    """
    raw = dagl_config.get("functions", {})
    default_platform = dagl_config.get("platform", "aws")
    default_region = dagl_config.get("region", "eu-central-1")
    
    functions_map = {}  # Legacy format: {name: string}
    functions_full = {}  # Full format: {name: dict with platform info}
    
    for func_name, func_value in raw.items():
        entry = normalize_function_entry(func_value, default_platform, default_region)
        functions_map[func_name] = entry["name"]
        functions_full[func_name] = entry
    
    return functions_map, functions_full


def is_multi_cloud_workflow(functions_full):
    """Check if the workflow spans multiple cloud platforms."""
    platforms = set(entry["platform"] for entry in functions_full.values())
    return len(platforms) > 1


# ─── Compile Command ─────────────────────────────────────────────────────────────

def cmd_compile(args):
    """Compile workflow.dag into per-function orchestration configs.
    
    In Layer mode, this doesn't generate files — it outputs the configs
    that will be set as env vars during deploy.
    """
    from dagl_to_unum_ir import compile_dagl_to_unum_ir
    
    workflow_file = args.workflow or "workflow.dag"
    if not os.path.exists(workflow_file):
        print(f"\033[31mError:\033[0m {workflow_file} not found.")
        sys.exit(1)
    
    with open(workflow_file) as f:
        source = f.read()
    
    print(f"Compiling {workflow_file}...")
    
    # Compile DAGL source → configs + template
    configs, template = compile_dagl_to_unum_ir(source)
    
    # Load function mapping from dagl.yaml
    dagl_config = load_dagl_config(args.config or "dagl.yaml")
    functions_map, functions_full = get_functions_map(dagl_config)
    
    # Validate all referenced functions have mappings
    missing = []
    for func_name in configs:
        if func_name not in functions_map:
            missing.append(func_name)
    
    if missing:
        print(f"\033[31mError:\033[0m Functions not mapped in dagl.yaml: {', '.join(missing)}")
        print(f"Add them to the 'functions:' section:")
        for m in missing:
            print(f"  {m}: <lambda-function-name-or-arn>")
        sys.exit(1)
    
    # Resolve actual runtimes from deployed Lambda functions (AWS only)
    import boto3
    region = dagl_config.get("region", "eu-central-1")
    lambda_client = boto3.client("lambda", region_name=region)
    for func_name, func_ref in functions_map.items():
        if func_name in template.get("Functions", {}):
            entry = functions_full[func_name]
            if entry["platform"] != "aws":
                continue  # Skip non-AWS functions for runtime resolution
            try:
                info = lambda_client.get_function_configuration(FunctionName=func_ref)
                actual_runtime = info.get("Runtime", "python3.11")
                template["Functions"][func_name]["Properties"]["Runtime"] = actual_runtime
            except Exception:
                pass  # keep default if function not yet deployed

    # Print summary
    start_functions = [name for name, cfg in configs.items() if cfg.get('Start', False)]
    end_functions = [name for name, cfg in configs.items() if 'Next' not in cfg]
    
    print(f"\n\033[36mWorkflow Summary:\033[0m")
    print(f"  Name: {template.get('Globals', {}).get('ApplicationName', '?')}")
    print(f"  Functions: {len(configs)} ({', '.join(configs.keys())})")
    print(f"  Start: {', '.join(start_functions)}")
    print(f"  End: {', '.join(end_functions)}")
    
    api_config = template.get("Globals", {}).get("Api")
    if api_config:
        print(f"  API: {api_config.get('method', 'POST')} {api_config.get('path', '/')}")
    
    event_config = template.get("Globals", {}).get("Event")
    if event_config:
        print(f"  EventBridge: {event_config.get('source')} / {event_config.get('detailType', '*')}")
    
    # Registry validation (if configured)
    registry = _get_registry(dagl_config)
    if registry:
        print(f"\n\033[36mRegistry Check:\033[0m")
        issues = registry.validate_workflow(configs, functions_map)
        if not issues:
            print(f"  \033[32m✓\033[0m All functions registered, I/O schemas compatible")
        else:
            for level, msg in issues:
                if level == "error":
                    print(f"  \033[31m✗\033[0m {msg}")
                else:
                    print(f"  \033[33m!\033[0m {msg}")
            errors = [i for i in issues if i[0] == "error"]
            if errors:
                print(f"\n\033[31mRegistry validation failed.\033[0m Fix errors above or register functions with 'dagl registry register'.")
    
    print(f"\n\033[32mCompilation OK.\033[0m Run 'dagl deploy' to apply.")
    
    # Write compiled output for deploy to use
    compiled = {
        "configs": configs,
        "template": template,
        "functions_map": functions_map,
        "functions_full": functions_full,
    }
    with open(".dagl-compiled.json", "w") as f:
        json.dump(compiled, f, indent=2)
    
    return compiled


# ─── AWS Lambda Function URL Helper ──────────────────────────────────────────────

def _ensure_function_url(lambda_client, func_ref):
    """Ensure a Lambda Function URL is enabled. Returns the URL.
    
    Enables Function URL with AuthType=NONE for cross-platform invocation.
    If already enabled, returns the existing URL.
    """
    try:
        resp = lambda_client.get_function_url_config(FunctionName=func_ref)
        return resp["FunctionUrl"]
    except lambda_client.exceptions.ResourceNotFoundException:
        pass
    
    # Create Function URL
    resp = lambda_client.create_function_url_config(
        FunctionName=func_ref,
        AuthType='NONE',  # Public for simplicity; production should use AWS_IAM
        InvokeMode='BUFFERED',
    )
    url = resp["FunctionUrl"]
    
    # Add permission for public access
    try:
        lambda_client.add_permission(
            FunctionName=func_ref,
            StatementId='FunctionURLAllowPublicAccess',
            Action='lambda:InvokeFunctionUrl',
            Principal='*',
            FunctionUrlAuthType='NONE',
        )
    except lambda_client.exceptions.ResourceConflictException:
        pass  # Permission already exists
    
    return url


# ─── GCP Cloud Functions Deploy Helper ───────────────────────────────────────────

def _deploy_gcp_function(func_name, func_ref, func_config, entry,
                         function_map_json, ds_type, ds_name,
                         workflow_name, checkpoint, faas_platform):
    """Deploy DAGL orchestration to an existing GCP Cloud Function.
    
    Strategy:
    1. Create a temporary deploy directory with user source + DAGL runtime
    2. Use gcloud CLI to redeploy with DAGL entry point and env vars
    """
    import subprocess
    import shutil
    import tempfile
    
    gcp_project = entry.get("project", "")
    gcp_region = entry.get("region", "europe-west1")
    runtime = entry.get("runtime", "python312")
    
    # Original entry point (user's handler)
    original_handler = entry.get("handler", "main.handler")
    
    # DAGL entry point based on runtime family
    if "python" in runtime:
        dagl_entry_point = "gcp_http_handler"
    elif "node" in runtime:
        dagl_entry_point = "gcpHttpHandler"
    else:
        dagl_entry_point = "gcp_http_handler"
    
    # Find gcloud executable
    gcloud_cmd = _find_gcloud()
    
    # Build env vars for gcloud
    env_vars = {
        "DAGL_USER_HANDLER": original_handler,
        "DAGL_CONFIG": json.dumps(func_config),
        "DAGL_FUNCTION_MAP": function_map_json,
        "FAAS_PLATFORM": faas_platform,
        "FAAS_PLATFORM_NATIVE": "gcp",
        "UNUM_INTERMEDIARY_DATASTORE_TYPE": ds_type,
        "UNUM_INTERMEDIARY_DATASTORE_NAME": ds_name,
        "WORKFLOW_NAME": workflow_name,
        "CHECKPOINT": str(checkpoint).lower(),
        "GC": "false",
    }
    
    # If multi-cloud, pass AWS credentials so GCP can invoke AWS Lambda directly
    if faas_platform == "multi":
        import boto3.session
        session = boto3.session.Session()
        creds = session.get_credentials()
        if creds:
            frozen = creds.get_frozen_credentials()
            if frozen.access_key:
                env_vars["AWS_ACCESS_KEY_ID"] = frozen.access_key
            if frozen.secret_key:
                env_vars["AWS_SECRET_ACCESS_KEY"] = frozen.secret_key
            if frozen.token:
                env_vars["AWS_SESSION_TOKEN"] = frozen.token
        # Also pass default region
        aws_region = session.region_name or os.environ.get("AWS_DEFAULT_REGION", "eu-central-1")
        env_vars["AWS_DEFAULT_REGION"] = aws_region
    
    # Determine source directory
    source_dir = entry.get("source")
    
    # Create a temp deploy dir that bundles user source + DAGL runtime
    deploy_dir = tempfile.mkdtemp(prefix="dagl_gcp_")
    try:
        # Get user source: from local dir or download from existing GCP function
        if source_dir and os.path.isdir(source_dir):
            for item in os.listdir(source_dir):
                s = os.path.join(source_dir, item)
                d = os.path.join(deploy_dir, item)
                if os.path.isdir(s):
                    shutil.copytree(s, d)
                else:
                    shutil.copy2(s, d)
        else:
            # Download source from existing deployed GCP function
            _download_gcp_function_source(gcloud_cmd, func_ref, gcp_region, gcp_project, deploy_dir)
        
        # Bundle DAGL Python runtime into the deploy directory
        if "python" in runtime:
            _bundle_dagl_python_runtime(deploy_dir)
        elif "node" in runtime:
            _bundle_dagl_nodejs_runtime(deploy_dir)
        
        # Write env vars file for gcloud (YAML format)
        env_file = os.path.join(deploy_dir, ".env.yaml")
        with open(env_file, "w") as f:
            for k, v in env_vars.items():
                # Quote all values to avoid YAML parsing issues with JSON strings
                escaped = str(v).replace("'", "''")
                f.write(f"{k}: '{escaped}'\n")
        
        # Build gcloud command
        cmd = [
            gcloud_cmd, "functions", "deploy", func_ref,
            f"--region={gcp_region}",
            f"--entry-point={dagl_entry_point}",
            f"--env-vars-file={env_file}",
            f"--source={deploy_dir}",
            "--trigger-http",
            "--allow-unauthenticated",
            "--quiet",
        ]
        
        if gcp_project:
            cmd.append(f"--project={gcp_project}")
        
        if runtime:
            cmd.append(f"--runtime={runtime}")
        
        # Execute gcloud deploy
        result = subprocess.run(cmd, capture_output=True, text=True, timeout=300)
        
        if result.returncode != 0:
            error_msg = result.stderr.strip() or result.stdout.strip()
            raise RuntimeError(f"gcloud: {error_msg}")
        
    finally:
        shutil.rmtree(deploy_dir, ignore_errors=True)
    
    return True


def _find_gcloud():
    """Find the gcloud CLI executable. Returns the full path."""
    import shutil as _shutil
    
    # Try PATH first
    gcloud = _shutil.which("gcloud")
    if gcloud:
        return gcloud
    
    # Common Windows install locations
    candidates = [
        os.path.join(os.environ.get("ProgramFiles(x86)", ""), "Google", "Cloud SDK", "google-cloud-sdk", "bin", "gcloud.cmd"),
        os.path.join(os.environ.get("ProgramFiles", ""), "Google", "Cloud SDK", "google-cloud-sdk", "bin", "gcloud.cmd"),
        os.path.join(os.environ.get("LOCALAPPDATA", ""), "Google", "Cloud SDK", "google-cloud-sdk", "bin", "gcloud.cmd"),
    ]
    
    for c in candidates:
        if c and os.path.isfile(c):
            return c
    
    raise FileNotFoundError(
        "gcloud CLI not found. Install it from https://cloud.google.com/sdk/docs/install "
        "or add it to PATH."
    )


def _download_gcp_function_source(gcloud_cmd, func_name, region, project, dest_dir):
    """Download the source of an existing GCP Cloud Function into dest_dir.
    
    Uses `gcloud functions describe` to get the source URL, then downloads and
    extracts it. This allows deploying DAGL orchestration to functions where the
    user doesn't have the source locally (e.g., deployed by another team).
    """
    import subprocess
    import zipfile
    import urllib.request
    
    # Get function details
    cmd = [gcloud_cmd, "functions", "describe", func_name,
           f"--region={region}", "--format=json"]
    if project:
        cmd.append(f"--project={project}")
    
    result = subprocess.run(cmd, capture_output=True, text=True, timeout=30)
    if result.returncode != 0:
        raise RuntimeError(
            f"Cannot describe GCP function '{func_name}': {result.stderr.strip()}")
    
    import json as _json
    info = _json.loads(result.stdout)
    
    # Gen2 functions store source in buildConfig.source.storageSource
    build_config = info.get("buildConfig", {})
    source_info = build_config.get("source", {}).get("storageSource", {})
    bucket = source_info.get("bucket", "")
    obj = source_info.get("object", "")
    
    if not bucket or not obj:
        # Try gen1 format
        source_archive = info.get("sourceArchiveUrl", "")
        if source_archive:
            # Direct GCS URL: gs://bucket/object
            pass  # Will use gsutil below
        else:
            raise RuntimeError(
                f"Cannot determine source for GCP function '{func_name}'. "
                f"Specify 'source' in dagl.yaml to provide the local source directory.")
    
    # Download source zip using gsutil/gcloud storage
    zip_path = os.path.join(dest_dir, "_source.zip")
    
    if bucket and obj:
        gs_url = f"gs://{bucket}/{obj}"
    else:
        gs_url = source_archive
    
    dl_cmd = [gcloud_cmd, "storage", "cp", gs_url, zip_path, "--quiet"]
    result = subprocess.run(dl_cmd, capture_output=True, text=True, timeout=60)
    
    if result.returncode != 0:
        raise RuntimeError(
            f"Cannot download source for '{func_name}': {result.stderr.strip()}")
    
    # Extract zip into dest_dir
    with zipfile.ZipFile(zip_path, 'r') as zf:
        zf.extractall(dest_dir)
    
    os.remove(zip_path)


def _bundle_dagl_python_runtime(deploy_dir):
    """Copy DAGL Python runtime files into a GCP function deploy directory.
    
    Also creates a top-level main.py that imports gcp_http_handler from the
    DAGL runtime, so gcloud --entry-point=gcp_http_handler can find it.
    """
    import shutil as _shutil
    
    # Find the DAGL runtime source
    cli_dir = os.path.dirname(os.path.abspath(__file__))
    runtime_dir = os.path.join(cli_dir, "..", "layer", "python", "dagl_runtime")
    runtime_dir = os.path.normpath(runtime_dir)
    
    if not os.path.isdir(runtime_dir):
        raise FileNotFoundError(f"DAGL Python runtime not found at {runtime_dir}")
    
    # Copy dagl_runtime package into deploy dir (replace if already exists)
    dest = os.path.join(deploy_dir, "dagl_runtime")
    if os.path.isdir(dest):
        _shutil.rmtree(dest)
