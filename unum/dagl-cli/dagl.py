def _registry_configure(args):
    """Add or update registry config in dagl.yaml."""
    import yaml

    config_path = args.config or "dagl.yaml"
    url = args.url or f"redis://{args.host or 'localhost'}:{args.port or 6379}/{args.db or 0}"

    # Test connection first
    try:
        from registry import Registry
        reg = Registry(url=url)
        print(f"\033[32m✓\033[0m Connected to Redis: {url}")
    except ImportError:
        print("\033[31mError:\033[0m redis package not installed. Run: pip install redis")
        sys.exit(1)
    except Exception as e:
        print(f"\033[31mError:\033[0m Cannot connect to {url}: {e}")
        sys.exit(1)

    # Update dagl.yaml
    if os.path.exists(config_path):
        with open(config_path) as f:
            config = yaml.safe_load(f) or {}
    else:
        config = {}

    config["registry"] = {"url": url}

    with open(config_path, "w") as f:
        yaml.dump(config, f, default_flow_style=False, sort_keys=False)

    print(f"\033[32m✓\033[0m Registry configured in {config_path}")


def _registry_list(args):
    """List all functions in the registry."""
    dagl_config = load_dagl_config(args.config or "dagl.yaml")
    registry = _get_registry(dagl_config)
    if not registry:
        print("\033[31mError:\033[0m Registry not configured. Run: dagl registry configure --url redis://...")
        sys.exit(1)

    account_filter = args.account if hasattr(args, 'account') and args.account else None
    names = registry.list_functions(account_id=account_filter)

    if not names:
        print("No functions registered.")
        return

    print(f"\033[36mRegistered Functions:\033[0m ({len(names)})\n")
    for name in names:
        info = registry.lookup(name)
        if not info:
            continue
        runtime = info.get("runtime", "?")
        region = info.get("region", "?")
        deployed = info.get("deployed_at", "?")
        has_input = "input_schema" in info
        has_output = "output_schema" in info
        schema_indicator = ""
        if has_input or has_output:
            parts = []
            if has_input:
                parts.append("in")
            if has_output:
                parts.append("out")
            schema_indicator = f" [{'/'.join(parts)}]"

        print(f"  \033[32m●\033[0m {name}")
        print(f"    {runtime} | {region} | deployed {deployed}{schema_indicator}")
        if has_input:
            print(f"    Input:  {info['input_schema']}")
        if has_output:
            print(f"    Output: {info['output_schema']}")
        print()


def _registry_check(args):
    """Check if all workflow functions are registered and I/O schemas match."""
    dagl_config = load_dagl_config(args.config or "dagl.yaml")
    registry = _get_registry(dagl_config)
    if not registry:
        print("\033[31mError:\033[0m Registry not configured. Run: dagl registry configure --url redis://...")
        sys.exit(1)

    # Load compiled workflow (or compile on the fly)
    compiled_path = ".dagl-compiled.json"
    if not os.path.exists(compiled_path):
        print("No compiled workflow found. Run 'dagl compile' first.")
        sys.exit(1)

    with open(compiled_path) as f:
        compiled = json.load(f)

    configs = compiled["configs"]
    functions_map = compiled["functions_map"]

    print(f"\033[36mRegistry Validation:\033[0m\n")

    # 1. Check all functions exist
    all_ok = True
    for func_name, func_ref in functions_map.items():
        if registry.check_deployed(func_ref):
            info = registry.lookup(func_ref)
            print(f"  \033[32m✓\033[0m {func_name} → {func_ref} ({info.get('runtime', '?')})")
        else:
            print(f"  \033[31m✗\033[0m {func_name} → {func_ref} (NOT REGISTERED)")
            all_ok = False

    # 2. Validate I/O schemas
    print()
    issues = registry.validate_workflow(configs, functions_map)
    if not issues:
        print(f"  \033[32m✓\033[0m All I/O schemas compatible")
    else:
        for level, msg in issues:
            if level == "error":
                print(f"  \033[31m✗\033[0m {msg}")
                all_ok = False
            else:
                print(f"  \033[33m!\033[0m {msg}")

    if all_ok:
        print(f"\n\033[32mAll checks passed.\033[0m")
    else:
        print(f"\n\033[31mValidation failed.\033[0m")
        sys.exit(1)


def _registry_register(args):
    """Register an individual Lambda function in the registry."""
    import boto3

    dagl_config = load_dagl_config(args.config or "dagl.yaml")
    registry = _get_registry(dagl_config)
    if not registry:
        print("\033[31mError:\033[0m Registry not configured. Run: dagl registry configure --url redis://...")
        sys.exit(1)

    func_name = args.function
    region = dagl_config.get("region", "eu-central-1")
    lambda_client = boto3.client("lambda", region_name=region)

    try:
        info = lambda_client.get_function_configuration(FunctionName=func_name)
    except Exception as e:
        print(f"\033[31mError:\033[0m Cannot read function '{func_name}': {e}")
        sys.exit(1)

    account_id = _get_account_id(region)
    arn = info["FunctionArn"]
    runtime = info.get("Runtime", "unknown")
    handler = info["Handler"]

    # Parse optional schemas from --input / --output
    input_schema = json.loads(args.input_schema) if args.input_schema else None
    output_schema = json.loads(args.output_schema) if args.output_schema else None

    # Auto-infer schemas from source code if not explicitly provided
    inferred = False
    if not input_schema or not output_schema:
        try:
            from schema_inference import infer_schemas_from_lambda
            inferred_in, inferred_out, _ = infer_schemas_from_lambda(lambda_client, func_name)
            if not input_schema and inferred_in:
                input_schema = inferred_in
                inferred = True
            if not output_schema and inferred_out:
                output_schema = inferred_out
                inferred = True
        except Exception as e:
            print(f"  \033[33m!\033[0m Schema inference failed: {e}")

    registry.register(
        func_name,
        arn=arn,
        runtime=runtime,
        handler=handler,
        region=region,
        account_id=account_id,
        input_schema=input_schema,
        output_schema=output_schema,
    )

    print(f"\033[32m✓\033[0m Registered: {func_name}")
    print(f"  ARN: {arn}")
    print(f"  Runtime: {runtime}")
    if input_schema:
        source_tag = " (inferred)" if inferred else ""
        print(f"  Input:  {input_schema}{source_tag}")
    if output_schema:
        source_tag = " (inferred)" if inferred else ""
        print(f"  Output: {output_schema}{source_tag}")


def _registry_schema(args):
    """Set input/output schemas for a registered function."""
    dagl_config = load_dagl_config(args.config or "dagl.yaml")
    registry = _get_registry(dagl_config)
    if not registry:
        print("\033[31mError:\033[0m Registry not configured.")
        sys.exit(1)

    func_name = args.function

    if not registry.check_deployed(func_name):
        print(f"\033[31mError:\033[0m '{func_name}' is not registered. Register it first with: dagl registry register {func_name}")
        sys.exit(1)

    input_schema = json.loads(args.input_schema) if args.input_schema else None
    output_schema = json.loads(args.output_schema) if args.output_schema else None

    if not input_schema and not output_schema:
        # Show current schemas
        info = registry.lookup(func_name)
        print(f"\033[36mSchemas for {func_name}:\033[0m")
        if "input_schema" in info:
            print(f"  Input:  {json.dumps(info['input_schema'])}")
        else:
            print(f"  Input:  (not set)")
        if "output_schema" in info:
            print(f"  Output: {json.dumps(info['output_schema'])}")
        else:
            print(f"  Output: (not set)")
        return

    registry.set_schema(func_name, input_schema=input_schema, output_schema=output_schema)
    print(f"\033[32m✓\033[0m Schemas updated for {func_name}")
    if input_schema:
        print(f"  Input:  {json.dumps(input_schema)}")
    if output_schema:
        print(f"  Output: {json.dumps(output_schema)}")


