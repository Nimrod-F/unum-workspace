def detect_runtime_family(runtime_str: str) -> str:
    """Detect 'python' or 'nodejs' from a Lambda runtime string like 'python3.13' or 'nodejs20.x'."""
    if runtime_str.startswith("python"):
        return "python"
    if runtime_str.startswith("nodejs"):
        return "nodejs"
    raise RuntimeError(f"Unsupported Lambda runtime: {runtime_str}. DAGL supports python and nodejs.")


def resolve_latest_layer_arn(layer_name: str, region: str) -> str:
    """Resolve a layer name to the latest published version ARN."""
    import boto3
    client = boto3.client("lambda", region_name=region)
    resp = client.list_layer_versions(LayerName=layer_name, MaxItems=1)
    versions = resp.get("LayerVersions", [])
    if not versions:
        raise RuntimeError(f"No versions found for layer '{layer_name}' in {region}")
    return versions[0]["LayerVersionArn"]


# Node.js wrapper content - injected into function code package
NODEJS_WRAPPER_JS = """\
// DAGL Runtime Wrapper - Auto-generated by 'dagl deploy'
// This file bridges the Lambda handler to the DAGL runtime Layer.
// require() resolves 'dagl_runtime/main' via NODE_PATH (/opt/nodejs/node_modules)
module.exports = require('dagl_runtime/main');
"""


def _inject_nodejs_wrapper(lambda_client, func_ref):
    """Download function code, inject dagl_wrapper.js, re-upload.
    
    Returns True on success. Lambda Node.js handler resolution only looks in
    /var/task (function code), not in Layer paths. So we inject a tiny wrapper
    that require()'s the Layer module.
    """
    # Get current code download URL
    resp = lambda_client.get_function(FunctionName=func_ref)
    code_url = resp["Code"]["Location"]
    
    # Download the zip
    with urlopen(code_url) as r:
        original_zip = r.read()
    
    # Add dagl_wrapper.js to the zip
    buf = io.BytesIO()
    with zipfile.ZipFile(io.BytesIO(original_zip), 'r') as zin:
        with zipfile.ZipFile(buf, 'w', zipfile.ZIP_DEFLATED) as zout:
            for item in zin.infolist():
                if item.filename == 'dagl_wrapper.js':
                    continue  # Replace if exists
                zout.writestr(item, zin.read(item.filename))
            zout.writestr('dagl_wrapper.js', NODEJS_WRAPPER_JS)
    
    # Upload modified code
    buf.seek(0)
    lambda_client.update_function_code(
        FunctionName=func_ref,
        ZipFile=buf.read(),
    )
    
    # Wait for update to complete
    import botocore
    waiter = lambda_client.get_waiter('function_updated_v2')
    waiter.wait(FunctionName=func_ref, WaiterConfig={'Delay': 2, 'MaxAttempts': 30})
    
    return True


def _remove_nodejs_wrapper(lambda_client, func_ref):
    """Remove dagl_wrapper.js from function code package."""
    resp = lambda_client.get_function(FunctionName=func_ref)
    code_url = resp["Code"]["Location"]
    
    with urlopen(code_url) as r:
        original_zip = r.read()
    
    buf = io.BytesIO()
    removed = False
    with zipfile.ZipFile(io.BytesIO(original_zip), 'r') as zin:
        with zipfile.ZipFile(buf, 'w', zipfile.ZIP_DEFLATED) as zout:
            for item in zin.infolist():
                if item.filename == 'dagl_wrapper.js':
                    removed = True
                    continue
                zout.writestr(item, zin.read(item.filename))
    
    if removed:
        buf.seek(0)
        lambda_client.update_function_code(
            FunctionName=func_ref,
            ZipFile=buf.read(),
        )
        waiter = lambda_client.get_waiter('function_updated_v2')
        waiter.wait(FunctionName=func_ref, WaiterConfig={'Delay': 2, 'MaxAttempts': 30})


